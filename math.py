d=np.subtract(b,a)
print(d)
print("#"*100)
f=np.multiply(b,a)
print(f)
g=np.divide(a,b)
print(g)
s=np.square(a)
print(s)
sq=np.sqrt(a)
print(sq)
dp=np.dot(a,b)
print(dp)
